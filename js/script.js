document.addEventListener("DOMContentLoaded", () => {
  // Preloader
  const preloader = document.querySelector(".preloader");
  if (preloader) {
    window.addEventListener("load", () => {
      preloader.classList.add("loaded");
      setTimeout(() => {
        preloader.remove();
      }, 500);
    });
  }

  // Navbar Sticky & Active Link
  const navbar = document.getElementById("navbar");
  const navLinks = document.querySelectorAll(".nav-links a");
  const sections = document.querySelectorAll("section");

  function changeActiveLink() {
    let current = "";
    sections.forEach((section) => {
      const sectionTop = section.offsetTop;
      if (scrollY >= sectionTop - 150) {
        current = section.getAttribute("id");
      }
    });

    navLinks.forEach((a) => {
      a.classList.remove("active");
      if (a.getAttribute("href").includes(current)) {
        a.classList.add("active");
      }
    });
  }

  window.addEventListener("scroll", () => {
    if (window.scrollY > 50) {
      navbar.style.boxShadow = "var(--shadow-lg)";
    } else {
      navbar.style.boxShadow = "var(--shadow-md)";
    }
    changeActiveLink();
  });

  // Smooth Scroll
  navLinks.forEach((link) => {
    link.addEventListener("click", function (e) {
      e.preventDefault();
      const targetId = this.getAttribute("href");
      const targetSection = document.querySelector(targetId);
      if (targetSection) {
        window.scrollTo({
          top: targetSection.offsetTop - 80,
          behavior: "smooth",
        });
      }
    });
  });

  // Mobile Menu Toggle
  const navToggle = document.getElementById("navToggle");
  const navMenu = document.querySelector(".nav-links");
  if (navToggle && navMenu) {
    navToggle.addEventListener("click", () => {
      navMenu.classList.toggle("active");
      navToggle.innerHTML = navMenu.classList.contains("active") ? '<i class="fas fa-times"></i>' : '<i class="fas fa-bars"></i>';
    });

    // Close menu when clicking outside
    document.addEventListener("click", (e) => {
      if (!navToggle.contains(e.target) && !navMenu.contains(e.target)) {
        navMenu.classList.remove("active");
        navToggle.innerHTML = '<i class="fas fa-bars"></i>';
      }
    });
  }

  // Back to Top Button
  const backToTopBtn = document.getElementById("backToTop");
  window.addEventListener("scroll", () => {
    if (window.scrollY > 300) {
      backToTopBtn.classList.add("show");
    } else {
      backToTopBtn.classList.remove("show");
    }
  });

  // Set current year in footer
  const yearSpan = document.getElementById("year");
  if (yearSpan) {
    yearSpan.textContent = new Date().getFullYear();
  }

  // Tab functionality
  const tabButtons = document.querySelectorAll(".tab-btn");
  tabButtons.forEach((button) => {
    button.addEventListener("click", () => {
      const tabName = button.dataset.tab;
      document.querySelectorAll(".tab-btn").forEach((btn) => btn.classList.remove("active"));
      document.querySelectorAll(".tab-content").forEach((content) => content.classList.remove("active"));
      button.classList.add("active");
      document.getElementById(`${tabName}-tab`).classList.add("active");
    });
  });

  // Animate progress bars
  const skillItems = document.querySelectorAll(".skill-item");
  const languageItems = document.querySelectorAll(".language-item");

  function animateProgressBars(items) {
    items.forEach((item) => {
      const percent = item.dataset.percent;
      const progressFill = item.querySelector(".progress-fill");
      if (progressFill) {
        progressFill.style.width = percent;
      }
    });
  }

  // Trigger animation when the section is in view
  const observerOptions = {
    root: null,
    rootMargin: "0px",
    threshold: 0.3,
  };

  const observer = new IntersectionObserver((entries, observer) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        if (entry.target.id === "about") {
          animateProgressBars(skillItems);
          animateProgressBars(languageItems);
        }
        observer.unobserve(entry.target);
      }
    });
  }, observerOptions);

  const aboutSection = document.getElementById("about");
  if (aboutSection) {
    observer.observe(aboutSection);
  }

  // Dark/Light Mode Toggle
  const themeToggle = document.getElementById("theme-toggle");
  const body = document.body;
  const currentTheme = localStorage.getItem("theme");

  // Check if user has a saved theme preference
  if (currentTheme === "dark") {
    body.classList.add("dark-mode");
    if (themeToggle) {
      themeToggle.querySelector("i").classList.replace("fa-moon", "fa-sun");
    }
  } else if (window.matchMedia && window.matchMedia("(prefers-color-scheme: dark)").matches) {
    // Check system preference
    body.classList.add("dark-mode");
    if (themeToggle) {
      themeToggle.querySelector("i").classList.replace("fa-moon", "fa-sun");
    }
    localStorage.setItem("theme", "dark");
  }

  if (themeToggle) {
    themeToggle.addEventListener("click", () => {
      body.classList.toggle("dark-mode");
      const isDarkMode = body.classList.contains("dark-mode");
      if (isDarkMode) {
        localStorage.setItem("theme", "dark");
        themeToggle.querySelector("i").classList.replace("fa-moon", "fa-sun");
      } else {
        localStorage.setItem("theme", "light");
        themeToggle.querySelector("i").classList.replace("fa-sun", "fa-moon");
      }
    });
  }

  // Language Switcher
  const languageSelect = document.getElementById("language-select");

  function applyTranslations(lang) {
    // Update all elements with data-lang-key attribute
    document.querySelectorAll("[data-lang-key]").forEach((element) => {
      const key = element.getAttribute("data-lang-key");
      if (translations[lang] && translations[lang][key]) {
        // Check if the element is an input element
        if (element.tagName === "INPUT" || element.tagName === "TEXTAREA") {
          element.value = translations[lang][key];
        } else {
          element.innerHTML = translations[lang][key];
        }
      }
    });

    // Update placeholders
    document.querySelectorAll("[data-lang-placeholder-key]").forEach((element) => {
      const key = element.getAttribute("data-lang-placeholder-key");
      if (translations[lang] && translations[lang][key]) {
        element.placeholder = translations[lang][key];
      }
    });

    // Update page title
    document.title = translations[lang].pageTitle;

    // Update HTML lang attribute
    document.documentElement.lang = lang;

    // Update modal content if open
    const modal = document.getElementById("portfolioModal");
    if (modal && modal.style.display === "flex") {
      const modalTitle = modal.querySelector(".modal-title");
      const modalImage = modal.querySelector(".modal-image");
      const modalDesc = modal.querySelector(".modal-desc");

      const activePortfolioItem = document.querySelector(".portfolio-item:hover") || document.querySelector(".portfolio-item.active");
      if (activePortfolioItem && modalTitle && modalImage && modalDesc) {
        const titleId = activePortfolioItem.dataset.titleId;
        const descId = activePortfolioItem.dataset.descId;

        if (titleId && descId && translations[lang][titleId] && translations[lang][descId]) {
          modalTitle.textContent = translations[lang][titleId];
          modalDesc.textContent = translations[lang][descId];
        }
      }
    }
  }

  // Set default language or retrieve from localStorage
  let storedLang = localStorage.getItem("language");
  if (!storedLang) {
    storedLang = "id"; // Default to Indonesian
    localStorage.setItem("language", storedLang);
  }

  if (languageSelect) {
    languageSelect.value = storedLang;
    applyTranslations(storedLang);

    languageSelect.addEventListener("change", (event) => {
      const selectedLang = event.target.value;
      localStorage.setItem("language", selectedLang);
      applyTranslations(selectedLang);
    });
  }

  // Contact Form Submission
  const contactForm = document.getElementById("contactForm");
  if (contactForm) {
    contactForm.addEventListener("submit", (e) => {
      e.preventDefault();

      // Get form data
      const formData = new FormData(contactForm);
      const name = formData.get("name");
      const email = formData.get("email");
      const subject = formData.get("subject");
      const message = formData.get("message");

      // Simulate form submission (in a real app, you would send this to a server)
      console.log("Contact Form Submitted:", { name, email, subject, message });

      // Show success message
      alert("Pesan Anda telah terkirim! Saya akan menghubungi Anda segera.");

      // Reset form
      contactForm.reset();
    });
  }

  // Newsletter Form
  const newsletterForm = document.querySelector(".newsletter-form");
  if (newsletterForm) {
    newsletterForm.addEventListener("submit", (e) => {
      e.preventDefault();
      const email = newsletterForm.querySelector("input[type='email']").value;
      console.log("Newsletter subscription:", email);
      alert("Terima kasih telah berlangganan newsletter!");
      newsletterForm.reset();
    });
  }

  // Dalam file script.js atau tambahkan di dalam <script> tags

  // Timeline animation on scroll
  const timelineItems = document.querySelectorAll(".timeline-item");

  function checkTimelineAnimation() {
    timelineItems.forEach((item, index) => {
      const itemPosition = item.getBoundingClientRect().top;
      const screenPosition = window.innerHeight / 1.3;

      if (itemPosition < screenPosition) {
        // Add delay for staggered animation
        setTimeout(() => {
          item.classList.add("visible");
        }, index * 200); // 200ms delay between each item
      }
    });
  }

  // Run on load and scroll
  window.addEventListener("load", checkTimelineAnimation);
  window.addEventListener("scroll", checkTimelineAnimation);

  // Navbar scroll effect
  let lastScroll = 0;
  window.addEventListener("scroll", () => {
    const navbar = document.getElementById("navbar");
    const currentScroll = window.pageYOffset;

    if (currentScroll > 50) {
      navbar.classList.add("scrolled");
      if (currentScroll > lastScroll) {
        // Scrolling down
        navbar.style.transform = "translateY(-100%)";
      } else {
        // Scrolling up
        navbar.style.transform = "translateY(0)";
      }
    } else {
      navbar.classList.remove("scrolled");
      navbar.style.transform = "translateY(0)";
    }

    lastScroll = currentScroll;
  });

  // Portfolio Modal
  const viewButtons = document.querySelectorAll(".view-btn");
  const portfolioModal = document.getElementById("portfolioModal");
  const modalClose = portfolioModal.querySelector(".close");

  viewButtons.forEach((button) => {
    button.addEventListener("click", (e) => {
      e.stopPropagation();
      const portfolioItem = button.closest(".portfolio-item");
      const modalTitle = portfolioModal.querySelector(".modal-title");
      const modalImage = portfolioModal.querySelector(".modal-image");
      const modalDesc = portfolioModal.querySelector(".modal-desc");

      const currentLang = localStorage.getItem("language") || "id";
      const titleId = portfolioItem.dataset.titleId;
      const imageSrc = portfolioItem.dataset.image;
      const descId = portfolioItem.dataset.descId;

      if (titleId && imageSrc && descId) {
        modalTitle.textContent = translations[currentLang][titleId];
        modalImage.src = imageSrc;
        modalImage.alt = translations[currentLang][titleId];
        modalDesc.textContent = translations[currentLang][descId];

        portfolioModal.style.display = "flex";
        document.body.style.overflow = "hidden";
      }
    });
  });

  if (modalClose) {
    modalClose.addEventListener("click", () => {
      portfolioModal.style.display = "none";
      document.body.style.overflow = "auto";
    });
  }

  // Close modal when clicking outside
  window.addEventListener("click", (e) => {
    if (e.target === portfolioModal) {
      portfolioModal.style.display = "none";
      document.body.style.overflow = "auto";
    }
  });

  // Close modal with Escape key
  document.addEventListener("keydown", (e) => {
    if (e.key === "Escape" && portfolioModal.style.display === "flex") {
      portfolioModal.style.display = "none";
      document.body.style.overflow = "auto";
    }
  });

  // Initialize animations for sections
  const sectionObserver = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("animated");
        }
      });
    },
    { threshold: 0.1 }
  );

  document.querySelectorAll(".section-card").forEach((card) => {
    sectionObserver.observe(card);
  });
});
